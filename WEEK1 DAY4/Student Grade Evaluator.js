// Accept student's marks
let marks = 72;   // Change this value to test different grades

// Grade evaluation using if-else
if (marks >= 75) {
    console.log("Marks: " + marks);
    console.log("Grade: A");
}
else if (marks >= 60) {
    console.log("Marks: " + marks);
    console.log("Grade: B");
}
else if (marks >= 40) {
    console.log("Marks: " + marks);
    console.log("Grade: C");
}
else {
    console.log("Marks: " + marks);
    console.log("Result: Fail");
}